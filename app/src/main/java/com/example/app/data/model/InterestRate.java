package com.example.app.data.model;

public class InterestRate {
    private double rate;

    public InterestRate(double rate) {
        this.rate = rate;
    }

    public double getRate() {
        return rate;
    }

    public double getSavingsRate() {
        return rate;
    }

    public double getMortgageRate() {
        return rate;
    }
}